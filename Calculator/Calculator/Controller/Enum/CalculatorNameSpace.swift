    static let Empty: String = ""
    static let Zero: String = "0"
    static let NaN: String = "NaN"
    static let Minus: String = "-"
    static let DoubleZero: String = "00"
    static let Period: String = "."
    static let Equal: String = "="
    static let Comma: String = ","
}
